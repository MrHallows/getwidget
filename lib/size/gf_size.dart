/// [GFSize] is used to specify the size of the widget.
class GFSize {
  /// [GFSize.SMALL] (32.0) is used to specify small sized widgets.
  static const double SMALL = 32;

  /// (Default) [GFSize.MEDIUM] (36.0) is used to specify medium sized widgets.
  static const double MEDIUM = 36;

  /// [GFSize.LARGE] (40.0) is used to specify large sized widgets.
  static const double LARGE = 40;
}
